            // Build dots
            slides.forEach((_, i) => {
                const d = document.createElement('div');
                d.className = 'rc-dot' + (i === 0 ? ' active' : '');
                d.addEventListener('click', () => goTo(i));
                dotsWrap.appendChild(d);
            });

            const updateDots = () => Array.from(dotsWrap.children).forEach((d, i) => d.classList.toggle('active', i === index));
            const width = () => track.getBoundingClientRect().width; // one slide width
            const apply = (previewPx = 0) => {
                // RTL: index 0 = rightmost position, move positively to show next slides
                const base = index * width();
                track.style.transform = `translateX(${base + previewPx}px)`;
            };
            const goTo = (i) => { index = (i + slides.length) % slides.length; track.style.transition = 'transform 450ms ease'; apply(0); updateDots(); };
            // In RTL UX, we still advance logically forward with +1 when moving visual left
            const nextSlide = () => goTo(index + 1);   // visual left
            const prevSlide = () => goTo(index - 1);   // visual right

            // RTL expectation: Left arrow (‹) moves visual RIGHT, Right arrow (›) moves visual LEFT
            prev && prev.addEventListener('click', prevSlide);
            next && next.addEventListener('click', nextSlide);

            const onDown = (e) => { dragging = true; deltaX = 0; startX = e.type.includes('mouse') ? e.pageX : e.touches[0].clientX; track.style.transition = 'none'; clearInterval(autoplayTimer); };
            const onMove = (e) => { if (!dragging) return; const x = e.type.includes('mouse') ? e.pageX : e.touches[0].clientX; deltaX = x - startX; apply(deltaX); };
            const onUp = () => { if (!dragging) return; dragging = false; const th = Math.min(140, width() * 0.18); if (isRTL) { if (deltaX < -th) prevSlide(); else if (deltaX > th) nextSlide(); else goTo(index); } else { if (deltaX < -th) nextSlide(); else if (deltaX > th) prevSlide(); else goTo(index); } startAutoplay(); };

            track.addEventListener('touchstart', onDown, { passive: true });
            track.addEventListener('touchmove', onMove, { passive: true });
            track.addEventListener('touchend', onUp);
            track.addEventListener('mousedown', onDown);
            window.addEventListener('mousemove', onMove);
            window.addEventListener('mouseup', onUp);
            window.addEventListener('resize', () => { track.style.transition = 'none'; apply(0); });

            const startAutoplay = () => { clearInterval(autoplayTimer); autoplayTimer = setInterval(nextSlide, 5000); };
            goTo(0);
            startAutoplay();
        });

