function getLanguage() {
  return localStorage.getItem('adly_lang') || DEFAULT_LANG;
}

function setLanguage(lang) {
  const normalized = I18N[lang] ? lang : DEFAULT_LANG;
  localStorage.setItem('adly_lang', normalized);
  const root = document.documentElement;
  root.setAttribute('lang', normalized);
  const isRtl = RTL_LANGS.includes(normalized);
  root.setAttribute('dir', isRtl ? 'rtl' : 'ltr');
  document.body.classList.toggle('rtl', isRtl);
  applyTranslations();
}

function t(key, vars = {}) {
  const lang = getLanguage();
  const dictionary = I18N[lang] || I18N[DEFAULT_LANG];
  let text = dictionary[key] || I18N[DEFAULT_LANG][key] || key;
  Object.keys(vars).forEach((name) => {
    text = text.replace(`{{${name}}}`, vars[name]);
  });
  return text;
}

function applyTranslations(scope = document) {
  scope.querySelectorAll('[data-i18n]').forEach((el) => {
    const key = el.getAttribute('data-i18n');
    if (!key) return;
    if (key === 'welcome_user' && el.dataset.i18nName) {
      el.textContent = t(key, { name: el.dataset.i18nName });
      return;
    }
    el.textContent = t(key);
  });
  scope.querySelectorAll('[data-i18n-placeholder]').forEach((el) => {
    const key = el.getAttribute('data-i18n-placeholder');
    if (key) el.setAttribute('placeholder', t(key));
  });
  scope.querySelectorAll('[data-i18n-title]').forEach((el) => {
    const key = el.getAttribute('data-i18n-title');
    if (key) el.setAttribute('title', t(key));
  });
}

function initLanguageSelector() {
  const selector = document.getElementById('lang-select');
  if (!selector) return;
  const current = getLanguage();
  selector.value = current;
  selector.addEventListener('change', (event) => {
    const value = event.target.value;
    setLanguage(value);
  });
}

document.addEventListener('DOMContentLoaded', () => {
  setLanguage(getLanguage());
  initLanguageSelector();
});

function getLuminance(hex) {
  if (!hex || typeof hex !== 'string') return 0.5;
  const cleaned = hex.replace('#', '');
  if (cleaned.length !== 6) return 0.5;
  const r = parseInt(cleaned.slice(0, 2), 16) / 255;
  const g = parseInt(cleaned.slice(2, 4), 16) / 255;
  const b = parseInt(cleaned.slice(4, 6), 16) / 255;
  const [lr, lg, lb] = [r, g, b].map((c) =>
    c <= 0.03928 ? c / 12.92 : Math.pow((c + 0.055) / 1.055, 2.4)
  );
  return 0.2126 * lr + 0.7152 * lg + 0.0722 * lb;
}

function setTheme(theme) {
  const root = document.documentElement;
  document.body.classList.remove('default-theme');
  const primary = theme.primary || DEFAULT_THEME.primary;
  const secondary = theme.secondary || DEFAULT_THEME.secondary;
  root.style.setProperty('--brand-primary', primary);
  root.style.setProperty('--brand-secondary', secondary);
  const avgLum = (getLuminance(primary) + getLuminance(secondary)) / 2;
  const onPrimary = avgLum > 0.6 ? '#111111' : '#ffffff';
  root.style.setProperty('--brand-on-primary', onPrimary);
  const logoEl = document.querySelector('[data-brand-logo]');
  const titleEl = document.querySelector('[data-brand-title]');
  if (logoEl) {
    const normalizedLogoUrl = normalizeUrl(theme.logoUrl);
    if (normalizedLogoUrl) {
      logoEl.innerHTML = `<img src="${normalizedLogoUrl}" alt="Brand logo">`;
    } else {
      logoEl.textContent = (theme.name || DEFAULT_THEME.name).slice(0, 1).toUpperCase();
    }
  }
  if (titleEl && theme.name) {
    titleEl.textContent = theme.name;
  }
}

function setDefaultTheme() {
  setTheme(DEFAULT_THEME);
  document.body.classList.add('default-theme');
}

