// Normalize URLs to current origin (handles production vs localhost)
function normalizeUrl(url) {
  if (!url) return url;
  // If it's already a relative path, return as-is
  if (url.startsWith('/')) return url;
  // Replace production domain with current origin
  if (url.startsWith('http://') || url.startsWith('https://')) {
    const origin = window.location.origin;
    return url.replace(/^https?:\/\/(getadly\.com|www\.getadly\.com|127\.0\.0\.1:\d+|localhost:\d+)/i, origin);
  }
  return url;
}

// Get logo URL from brand data - prefers logo_filename for relative path
function getLogoUrl(brand) {
  if (!brand) return null;
  // If logo_filename exists, construct relative URL (works on any domain)
  if (brand.logo_filename) {
    return '/uploads/logos/' + brand.logo_filename;
  }
  // Fall back to normalizing logo_url if it exists
  if (brand.logo_url) {
    return normalizeUrl(brand.logo_url);
  }
  return null;
}

function iconSvg(id, className = '') {
  const classes = ['icon', className].filter(Boolean).join(' ');
  return `<svg class="${classes}" aria-hidden="true"><use href="/ui/icons.svg?v=20260125b#${id}"></use></svg>`;
}
let appShellLoaded = false;
async function loadAppShellTemplate() {
  if (appShellLoaded) return;
  const shell = document.querySelector('.app-shell');
  if (!shell) return;
  const sidebar = shell.querySelector('.sidebar');
  const header = shell.querySelector('.app-header');
  if (!sidebar || !header) return;
  try {
    const response = await fetch('/ui/app-shell-fragment.html', { cache: 'no-store' });
    if (!response.ok) return;
    const html = await response.text();
    const doc = new DOMParser().parseFromString(html, 'text/html');
    const nextSidebar = doc.querySelector('.sidebar');
    const nextHeader = doc.querySelector('.app-header');
    if (nextSidebar) sidebar.replaceWith(nextSidebar);
    if (nextHeader) header.replaceWith(nextHeader);
    appShellLoaded = true;
  } catch (error) {
    // Keep existing shell if the template cannot be loaded.
  }
}
