function updateClientLinks(clientId) {
  document.querySelectorAll('[data-client-link]').forEach((link) => {
    const base = link.getAttribute('data-client-link');
    if (!base) return;
    if (!clientId) {
      link.setAttribute('href', base);
      return;
    }
    const url = new URL(base, window.location.origin);
    url.searchParams.set('client_id', clientId);
    link.setAttribute('href', url.pathname + '?' + url.searchParams.toString());
  });
}

function renderCreditPills(container, plan, limits = null) {
  if (!container) return;
  const items = [
    { icon: iconSvg('icon-pencil', 'credit-icon'), value: plan.text_credits_remaining, label: t('credits_text') },
    { icon: iconSvg('icon-image', 'credit-icon'), value: plan.image_credits_remaining, label: t('credits_image') },
    { icon: iconSvg('icon-video', 'credit-icon'), value: plan.video_credits_remaining, label: t('credits_video') },
    { icon: iconSvg('icon-globe', 'credit-icon'), value: plan.landing_credits_remaining, label: t('credits_landing') }
  ];
  if (limits) {
    const brandValue = limits.brands_limit > 0
      ? `${limits.brands_used}/${limits.brands_limit}`
      : `${limits.brands_used}/∞`;
    const cardsValue = limits.cards_enabled === false
      ? t('profile_not_available')
      : (limits.cards_limit > 0 ? `${limits.cards_used}/${limits.cards_limit}` : `${limits.cards_used}/∞`);
    const landingValue = limits.landing_limit > 0
      ? `${limits.landing_used}/${limits.landing_limit}`
      : `${limits.landing_used}/∞`;
    items.push(
      { icon: iconSvg('icon-library', 'credit-icon'), value: brandValue, label: t('credits_brands_limit') },
      { icon: iconSvg('icon-globe', 'credit-icon'), value: landingValue, label: t('credits_landing_limit') },
      { icon: iconSvg('icon-image', 'credit-icon'), value: cardsValue, label: t('credits_cards_limit') }
    );
  }
  container.innerHTML = items.map((item) => `
    <div class="credit-pill" data-tooltip="${item.label}: ${item.value ?? 0}" tabindex="0" aria-label="${item.label}: ${item.value ?? 0}">
      <span class="credit-icon">${item.icon}</span>
      <span class="credit-value">${item.value ?? 0}</span>
    </div>
  `).join('');
}

