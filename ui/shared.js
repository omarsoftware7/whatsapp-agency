function markActivePlanBadge(planTier) {
  const cards = document.querySelectorAll('.pricing-card, .plan-card');
  if (!cards.length) return;
  const normalized = (planTier || '').toLowerCase();
  const allowed = ['starter', 'growth', 'pro', 'agency'];
  cards.forEach((card) => {
    const existing = card.querySelector('.plan-badge');
    if (existing) existing.remove();
    card.classList.remove('current-plan');
    if (!allowed.includes(normalized)) return;
    if (card.dataset.tier !== normalized) return;
    const badge = document.createElement('span');
    badge.className = 'plan-badge';
    badge.dataset.i18n = 'pricing_your_plan';
    badge.textContent = t('pricing_your_plan');
    card.appendChild(badge);
    card.classList.add('current-plan');
  });
}

async function initAppShell(activeNav = '', options = {}) {
  await loadAppShellTemplate();
  setLanguage(getLanguage());
  initLanguageSelector();
  const userChip = document.getElementById('user-chip');
  const userWelcome = document.getElementById('user-welcome');
  const userName = document.getElementById('user-name');
  const userAvatar = document.getElementById('user-avatar');
  const navCredits = document.getElementById('nav-credits');
  const headerCredits = document.getElementById('header-credits');
  const mobileCredits = document.getElementById('mobile-credits');
  const brandSelect = document.getElementById('brand-select');
  const sidebarBrandName = document.getElementById('sidebar-brand-name');
  const sidebarBrandLogo = document.getElementById('sidebar-brand-logo');
  const logoutButton = document.getElementById('logout');
  const adminNav = document.getElementById('admin-nav');
  const navToggle = document.getElementById('nav-toggle');
  const sidebarClose = document.getElementById('sidebar-close');
  let sidebarOverlay = document.getElementById('sidebar-overlay');
  if (!sidebarOverlay) {
    sidebarOverlay = document.createElement('div');
    sidebarOverlay.className = 'sidebar-overlay';
    sidebarOverlay.id = 'sidebar-overlay';
    document.body.appendChild(sidebarOverlay);
  }

  let planLimits = null;
  try {
    const auth = await apiRequest('/web_auth.php', {
      method: 'POST',
      body: JSON.stringify({ action: 'me' })
    });
    const user = auth.user || {};
    const displayName = user.first_name || user.last_name || user.email || 'User';
    if (userWelcome && userName) {
      userWelcome.textContent = t('welcome_label');
      userName.textContent = displayName;
    } else if (userWelcome) {
      userWelcome.dataset.i18nName = displayName;
      userWelcome.textContent = t('welcome_user', { name: displayName });
    }
    if (userAvatar) {
      if (user.avatar_url) {
        userAvatar.innerHTML = `<img src="${user.avatar_url}" alt="User avatar">`;
      } else {
        userAvatar.textContent = (displayName || 'U').slice(0, 1).toUpperCase();
      }
    }
    markActivePlanBadge(user.plan_tier || '');
    if (adminNav && user.role === 'admin') {
      adminNav.style.display = 'flex';
    }
    const plan = {
      text_credits_remaining: Number(user.text_credits_remaining || 0),
      image_credits_remaining: Number(user.image_credits_remaining || 0),
      video_credits_remaining: Number(user.video_credits_remaining || 0),
      landing_credits_remaining: Number(user.landing_credits_remaining || 0)
    };
    renderCreditPills(navCredits, plan, planLimits);
    renderCreditPills(headerCredits, plan, planLimits);
    renderMobileCredits(mobileCredits, plan, planLimits);
    try {
      const limitsResponse = await apiRequest('/web_profile.php', {
        method: 'POST',
        body: JSON.stringify({ action: 'limits' })
      });
      planLimits = limitsResponse.limits || null;
    } catch (error) {
      planLimits = null;
    }
    renderCreditPills(navCredits, plan, planLimits);
    renderCreditPills(headerCredits, plan, planLimits);
    renderMobileCredits(mobileCredits, plan, planLimits);

    const brandData = await apiRequest('/web_brands.php');
    const brands = brandData.items || [];
    if (!brands.length && !options.allowEmptyBrands) {
      window.location.href = '/ui/onboarding.html';
      return;
    }
    // Common setup for logout, nav toggle, active nav - runs regardless of brands
    const setupCommonHandlers = () => {
      if (logoutButton) {
        logoutButton.addEventListener('click', async () => {
          try {
            await apiRequest('/web_auth.php', {
              method: 'POST',
              body: JSON.stringify({ action: 'logout' })
            });
          } catch (error) {
            // ignore
          }
          window.location.href = '/ui/login.html';
        });
      }
      if (activeNav) {
        const activeLink = document.querySelector(`.nav-item[data-nav="${activeNav}"]`);
        if (activeLink) activeLink.classList.add('active');
      }
      if (navToggle) {
        navToggle.addEventListener('click', () => document.body.classList.add('sidebar-open'));
      }
      if (sidebarClose) {
        sidebarClose.addEventListener('click', () => document.body.classList.remove('sidebar-open'));
      }
      if (sidebarOverlay) {
        sidebarOverlay.addEventListener('click', () => document.body.classList.remove('sidebar-open'));
      }
    };

    if (!brands.length) {
      setDefaultTheme();
      if (sidebarBrandName) sidebarBrandName.textContent = DEFAULT_THEME.name;
      if (sidebarBrandLogo) {
        // Show Adly default logo when no brands exist
        const logoWrap = sidebarBrandLogo.parentElement;
        if (logoWrap) {
          logoWrap.innerHTML = `<div id="sidebar-brand-logo" class="brand-logo-placeholder">Ad</div>`;
        }
      }
      if (brandSelect) brandSelect.innerHTML = '<option value="">No brands yet</option>';
      // Update nav links to work without a brand (no client_id param)
      updateClientLinks(null);
      setupCommonHandlers();
      return;
    }
    const params = new URLSearchParams(window.location.search);
    const fromQuery = params.get('client_id');
    const stored = getSelectedBrandId();
    let currentBrand = brands.find((b) => String(b.id) === String(fromQuery))
      || brands.find((b) => String(b.id) === String(stored))
      || brands[0];

    saveSelectedBrandId(currentBrand.id);
    if (brandSelect) {
      brandSelect.innerHTML = '';
      brands.forEach((brand) => {
        const option = document.createElement('option');
        option.value = brand.id;
        option.textContent = brand.business_name || `Brand ${brand.id}`;
        brandSelect.appendChild(option);
      });
      brandSelect.value = currentBrand.id;
    }

    const themeMode = user.theme_mode || 'brand';
    const brandLogoUrl = getLogoUrl(currentBrand);
    if (themeMode === 'default') {
      setDefaultTheme();
      updateBrandLogoWarning(currentBrand);
    } else {
      setTheme({
        primary: currentBrand.primary_color || DEFAULT_THEME.primary,
        secondary: currentBrand.secondary_color || DEFAULT_THEME.secondary,
        logoUrl: brandLogoUrl,
        name: currentBrand.business_name || DEFAULT_THEME.name
      });
      updateBrandLogoWarning(currentBrand);
    }
    if (sidebarBrandName) sidebarBrandName.textContent = currentBrand.business_name || DEFAULT_THEME.name;
    if (sidebarBrandLogo) {
      const logoWrap = sidebarBrandLogo.parentElement;
      if (brandLogoUrl) {
        if (logoWrap) {
          logoWrap.innerHTML = `<img id="sidebar-brand-logo" src="${brandLogoUrl}" alt="Brand logo">`;
        } else {
          sidebarBrandLogo.src = brandLogoUrl;
        }
      } else if (logoWrap) {
        // Show Adly placeholder when brand has no logo
        logoWrap.innerHTML = `<div id="sidebar-brand-logo" class="brand-logo-placeholder">Ad</div>`;
      }
    }
    updateClientLinks(currentBrand.id);

    if (brandSelect && !window.location.pathname.endsWith('/app.html')) {
      brandSelect.addEventListener('change', () => {
        const id = brandSelect.value;
        saveSelectedBrandId(id);
        const next = brands.find((b) => String(b.id) === String(id));
        if (!next) return;
        const nextParams = new URLSearchParams(window.location.search);
        nextParams.set('client_id', id);
        window.location.search = nextParams.toString();
      });
    }

    setupCommonHandlers();
  } catch (error) {
    if (userChip) userChip.style.display = 'none';
    window.location.href = '/ui/login.html';
  }
}

function showAlert(container, message, type = 'error') {
  let stack = document.getElementById('toast-stack');
  if (!stack) {
    stack = document.createElement('div');
    stack.id = 'toast-stack';
    document.body.appendChild(stack);
  }
  const toast = document.createElement('div');
  toast.className = `toast ${type}`;
  toast.innerHTML = `
    <div class="toast-message">${message}</div>
    <button class="toast-close" aria-label="Close">✕</button>
  `;
  const close = () => {
    toast.classList.add('hide');
    setTimeout(() => toast.remove(), 200);
  };
  const closeButton = toast.querySelector('.toast-close');
  if (closeButton) {
    closeButton.addEventListener('click', close);
  }
  stack.appendChild(toast);
  setTimeout(close, 5000);
  if (container) {
    container.innerHTML = '';
  }
}

function clearAlert(container) {
  if (!container) return;
  container.innerHTML = '';
}

